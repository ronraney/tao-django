
function toggleDivs() {
   var FrameOne = document.getElementById("FrameOne"); 
   var FrameTwo = document.getElementById("FrameTwo");

   FrameOne.style.display = (
       FrameOne.style.display == "none" ? "block" : "none"); 
   FrameTwo.style.display = (
       FrameTwo.style.display == "none" ? "block" : "none"); 
}

